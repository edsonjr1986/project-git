let corSemaforo = "vermelho";
if (corSemaforo === "verde") {
    console.log("Siga em frente!");
} else if (corSemaforo === "amarelo") {
    console.log("Atenção! Prepare-se para parar.");
} else if (corSemaforo === "vermelho") {
    console.log("Pare!");
} else {
    console.log("Cor inválida.");
}